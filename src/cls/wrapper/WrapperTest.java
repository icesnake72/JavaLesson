package cls.wrapper;

public class WrapperTest {
    public static void main(String[] args) {
        // primitive types
        // byte, short, char, int, long, float, double, long double

        // referenced types
        // custom user created types, String, Random ...

        // Wrapper Classes
        byte b1 = 10;   // Primitive Type
        Byte bt = b1;
        Short sh = 10;
        Character ch = 'A';  // char
        Integer number = 100;   // integer
        Float f1 = 3.14F;       // float

        System.out.println(bt);
        System.out.println(ch);
        System.out.println(number);
        System.out.println(f1);
    }
}
